class Solution {
  public:
    int distributeTicket(int N, int K) {
        // code here
        int left = 1;
       int right = N;
       while(left < right) {
           int temp = K;
           while(left < right && temp--) left++;
           temp = K;
           while(left < right && temp--) right--;
       }
       return left;
    }
};


